/**
  * @brief This function handles EXTI line 2 and 3 interrupts.
  */
void EXTI2_3_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI2_3_IRQn 0 */

  /* USER CODE END EXTI2_3_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_3);
  /* USER CODE BEGIN EXTI2_3_IRQn 1 */
	//get CR1 and change the dir
		uint32_t tim2cr1 = htim2.Instance->CR1;
		tim2cr1 &= ~(TIM_CR1_DIR | TIM_CR1_CMS);		
		if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_3) == GPIO_PIN_SET)				//PB3 is high,timer2 up cnt
		{
			tim2cr1 |= TIM_COUNTERMODE_UP;
		}
		else					//PB3 is low ,timer2 down cnt
		{
			tim2cr1 |= TIM_COUNTERMODE_DOWN;
		}
		htim2.Instance->CR1 = tim2cr1;
  /* USER CODE END EXTI2_3_IRQn 1 */
}

/**
  * @brief This function handles EXTI line 4 to 15 interrupts.
  */
void EXTI4_15_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI4_15_IRQn 0 */

  /* USER CODE END EXTI4_15_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_11);
  /* USER CODE BEGIN EXTI4_15_IRQn 1 */
		//get CR1 and change the dir
		uint32_t tim1cr1 = htim1.Instance->CR1;
		uint32_t tim3cr1 = htim3.Instance->CR1;
		
		tim1cr1 &= ~(TIM_CR1_DIR | TIM_CR1_CMS);
		tim3cr1 &= ~(TIM_CR1_DIR | TIM_CR1_CMS);
		if(HAL_GPIO_ReadPin(GPIOA,GPIO_PIN_11) == GPIO_PIN_SET)				//PA11 is high,timer1,3 up cnt
		{
			tim1cr1 |= TIM_COUNTERMODE_UP;
			tim3cr1 |= TIM_COUNTERMODE_UP;
		}
		else																					//PA11 is low ,timer1,3 down cnt
		{
			tim1cr1 |= TIM_COUNTERMODE_DOWN;
			tim3cr1 |= TIM_COUNTERMODE_DOWN;
		}
		htim1.Instance->CR1 = tim1cr1;
		htim3.Instance->CR1 = tim3cr1;
  /* USER CODE END EXTI4_15_IRQn 1 */
}

